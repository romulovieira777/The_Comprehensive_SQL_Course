SELECT
    SUM(SAL) AS "Total Salary"
FROM
    EMP;


SELECT
    AVG(SAL) AS "Average Salary"
FROM
    EMP;


SELECT
    MAX(SAL) AS "Maximum Salary"
  , MIN(SAL) AS "Minimum Salary"
FROM
    EMP;


SELECT
    *
FROM
    EMP;


SELECT
    MAX(ENAME) AS "Maximum Name"
FROM
    EMP;


SELECT
    MIN(ENAME) AS "Minimum Name"
FROM
    EMP;
