SELECT
    EBA_COUNTRIES.NAME
  , EBA_COUNTRIES.POPULATION
  , EBA_COUNTRIES.REGION_ID
  , EBA_COUNTRY_REGIONS.NAME
FROM
    EBA_COUNTRIES
LEFT JOIN
    EBA_COUNTRY_REGIONS
ON
    EBA_COUNTRIES.REGION_ID = EBA_COUNTRY_REGIONS.ID;


SELECT
    C.NAME
  , C.POPULATION
  , C.REGION_ID
  , CR.NAME
FROM
    EBA_COUNTRIES C
RIGHT JOIN
    EBA_COUNTRY_REGIONS CR
ON
    C.REGION_ID = CR.ID;

